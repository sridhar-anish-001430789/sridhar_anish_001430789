//Created a Car_Details Class and various attributes are defined
public class Car_Details {
    private String Model;
    private String Color;
    private String Serial_Number;
    private String Dimensions;
    private String Seating_Capacity;
    private String Mileage;
    private String Top_Speed;
    private String Engine;
    private String Weight;
    private String Category;
    private String Fuel_Tank_Capacity;
    private String Transmission;
    private String Gear;
    private String Price;
    private String Generation;
    private String Year;
    private String image_path;
    
    
    //Getters and Setters are used to get and set the attributes
    public String getImage_path() {
        return image_path;
    }

    public void setImage_path(String image_path) {
        this.image_path = image_path;
    }

    public String getModel() {
        return Model;
    }

    public void setModel(String Model) {
        this.Model = Model;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String Color) {
        this.Color = Color;
    }

    public String getSerial_Number() {
        return Serial_Number;
    }

    public void setSerial_Number(String Serial_Number) {
        this.Serial_Number = Serial_Number;
    }

    public String getDimensions() {
        return Dimensions;
    }

    public void setDimensions(String Dimensions) {
        this.Dimensions = Dimensions;
    }

    public String getSeating_Capacity() {
        return Seating_Capacity;
    }

    public void setSeating_Capacity(String Seating_Capacity) {
        this.Seating_Capacity = Seating_Capacity;
    }

    public String getMileage() {
        return Mileage;
    }

    public void setMileage(String Mileage) {
        this.Mileage = Mileage;
    }

    public String getTop_Speed() {
        return Top_Speed;
    }

    public void setTop_Speed(String Top_Speed) {
        this.Top_Speed = Top_Speed;
    }

    public String getEngine() {
        return Engine;
    }

    public void setEngine(String Engine) {
        this.Engine = Engine;
    }

    public String getWeight() {
        return Weight;
    }

    public void setWeight(String Weight) {
        this.Weight = Weight;
    }

    public String getCategory() {
        return Category;
    }

    public void setCategory(String Category) {
        this.Category = Category;
    }

    public String getFuel_Tank_Capacity() {
        return Fuel_Tank_Capacity;
    }

    public void setFuel_Tank_Capacity(String Fuel_Tank_Capacity) {
        this.Fuel_Tank_Capacity = Fuel_Tank_Capacity;
    }

    public String getTransmission() {
        return Transmission;
    }

    public void setTransmission(String Transmission) {
        this.Transmission = Transmission;
    }

    public String getGear() {
        return Gear;
    }

    public void setGear(String Gear) {
        this.Gear = Gear;
    }

    public String getPrice() {
        return Price;
    }

    public void setPrice(String Price) {
        this.Price = Price;
    }

    public String getGeneration() {
        return Generation;
    }

    public void setGeneration(String Generation) {
        this.Generation = Generation;
    }

    public String getYear() {
        return Year;
    }

    public void setYear(String Year) {
        this.Year = Year;
    }
}

    private void maxmin_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maxmin_btnActionPerformed
        // TODO add your handling code here:
        
        int max=Integer.parseInt(Max_field.getText());
        
        int min=Integer.parseInt(Min_field.getText());
   
         ArrayList<CarDetails> list1 = new ArrayList<CarDetails>();
         for(CarDetails Carr : cd.getCar())
         {
             if(min<=Carr.getSeating_capacity()&&Carr.getSeating_capacity()<=max)
             {
                 list1.add(Carr);
                 populatetable1(list1);
             }
         }
        
    }//GEN-LAST:event_maxmin_btnActionPerformed

    private void count_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_count_btnActionPerformed
        // TODO add your handling code here:
          int count=0;
          int count1=0;
         for(CarDetails Carr : cd.getCar())
            
         {
             if(Carr.getAvail_car().equalsIgnoreCase("Yes"))
                 count=count+1;
             else
             {
                 count1=count1+1;
             }
         }
         availCars_field.setText(count+"");
         UnavailCars_field.setText(count1+"");
         
    }//GEN-LAST:event_count_btnActionPerformed


    private void catupd_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_catupd_btnActionPerformed
        // TODO add your handling code here:
         Date c;
         c=cd.getCar().get(0).getCreated_On();
         SimpleDateFormat s=new SimpleDateFormat("HH:mm:ss");
         for(CarDetails Carr : cd.getCar())
         {
             if(Carr.getCreated_On().compareTo(c)>0)
             {
                 catupd_field.setText(s.format(Carr.getCreated_On()));
             }
              if(Carr.getCreated_On().compareTo(c)<0)
             {
                 catupd_field.setText(s.format(c));
             }
               if(Carr.getCreated_On().compareTo(c)==0)
             {
                 catupd_field.setText(s.format(Carr.getCreated_On()));
             }
         }    
    }//GEN-LAST:event_catupd_btnActionPerformed


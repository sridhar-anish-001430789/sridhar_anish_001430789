    private void avail_cars_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avail_cars_btnActionPerformed
        // TODO add your handling code here:
       ArrayList<CarDetails> list = new ArrayList<CarDetails>();
       for(CarDetails Carr : cd.getCar() )   // add matched element to list and populate table 
       {
           if(Carr.getAvail_car().equalsIgnoreCase("Yes"))
           {
               list.add(Carr);
               populatetable1(list);
           }
           
       }
    }
       private void populatetable1(ArrayList<CarDetails>alist)
       {
           DefaultTableModel dtm=(DefaultTableModel)Car_table2.getModel();
        dtm.setRowCount(0);
        for(CarDetails Carr : alist)
        {
            Object[]row=new Object[8];
            row[0]=Carr;
            row[1]=Carr.getCar_Manufacturers();
            row[2]=Carr.getSerial_Number();
            row[3]=Carr.getSeating_capacity();
            row[4]=Carr.getCar_City();
            row[5]=Carr.getYear_Manufacture();
            row[6]=Carr.getAvail_car();
            row[7]=Carr.getCar_Expired();
            
            
            dtm.addRow(row);
            
        }
           
    }//GEN-LAST:event_avail_cars_btnActionPerformed

